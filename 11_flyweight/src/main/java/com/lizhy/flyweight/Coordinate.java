package com.lizhy.flyweight;

/**
 * NOTE:非共享享元类（位置）
 *
 * @author lizhiyang
 * @Date 2018-07-21 17:31
 */
public class Coordinate {
    private int x;
    private int y;

    public Coordinate(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
